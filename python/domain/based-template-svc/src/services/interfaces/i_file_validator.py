from abc import ABC, abstractmethod
from typing import IO


class IFileValidator(ABC):
    """
    ファイルを検証するためのインターフェイスクラスです。
    与えられたファイル(バイナリ)とファイル拡張子に基づいて、
    各種バリデーション(サイズ上限、必須列、ページ数、音声長など)を行います。
    """

    @abstractmethod
    def file_validate_check(self, file: IO, file_type: str) -> None:
        """
        指定されたファイルおよび拡張子を総合的に検証し、以下の項目に違反がないか確認します。

        1. **ファイル形式のサポート可否**
           - 想定外の拡張子の場合は HTTP 415 を発生させます。
             例: pdf, doc, docx, csv, xlsx, xls, wav, mp3, flac, aac, mp4 など

        2. **ファイルサイズの上限超過**
           - 例: 100MB (100 * 1024 * 1024 Bytes) を超える場合は HTTP 413 を発生させます。
           - 動画ファイルは 1GB まで許可されます。

        3. **PDF ファイルのページ数上限**
           - 例: 300ページを超える場合は HTTP 423 (あるいは独自定義のコード) を発生させます。

        4. **Word ファイル(doc/docx)のページ数上限**
           - 例: 300ページを超える場合は HTTP 424 (あるいは独自定義のコード) を発生させます。
           - python-docx はレイアウト情報を正確に扱わないため、簡易的な算出方法を用います。

        5. **音声ファイル(wav, mp3, flac, aac)の長さ上限**
           - 例: 600秒(10分)を超える場合は HTTP 425 (あるいは独自定義のコード) を発生させます。

        6. **ファイル読み込み/解析エラー**
           - PDF解析、Word解析、音声解析などでエラーが起きた場合、
             HTTP 426 (あるいは独自定義のコード) を発生させます。

        7. **その他の想定外エラー**
           - 上記以外でキャッチできない例外は HTTP 500 として扱います。

        バリデーションが正常に終了した場合、例外は発生しません。

        Args:
            file (IO):
                検証対象のファイルオブジェクト。バイナリモードで開かれている必要があります。
            file_type (str):
                拡張子を小文字で指定してください。
                例: "csv", "xlsx", "pdf", "doc", "docx", "wav", "mp3", "flac", "aac", "mp4" など。

        Raises:
            HTTPException:
                - 415: サポート外のファイル形式 (Unsupported Media Type)
                - 413: ファイルサイズが上限を超過 (Request Entity Too Large)
                - 423: PDFのページ数超過
                - 424: Wordファイルのページ数超過
                - 425: 音声ファイルの長さ超過
                - 426: ファイル読み込み・解析エラー
                - 500: 想定外のエラー
        """
        pass
